#include <stdio.h>

int main()
{
    int a = 2, b = 3, sum, difference, multiplication;
    sum = a + b;
    difference = a - b;
    multiplication = a * b;

    printf("\n Sum of two numbers = %d", sum);
    printf("\n Difference of two numbers = %d", difference);
    printf("\n Multiplication of two numbers = %d", multiplication);

    return 0;
}